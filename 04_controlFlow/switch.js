const month = 3

switch (month) {
    case 1:
        console.log("JAN")
        break;
    case 2:
        console.log("FEB")
        break;
    case 3:
        console.log("MAR")
        break;
    case 4:
        console.log("APR")
        break;
    default:
        console.log("DEFAULT CASE MATCH")
        break;
}